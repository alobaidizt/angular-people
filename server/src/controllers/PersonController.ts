

import express = require("express");
import PersonBusiness = require("./../app/business/PersonBusiness");
import IBaseController = require("./BaseController");
import IPersonModel = require("./../app/model/interfaces/PersonModel");

class PersonController implements IBaseController <PersonBusiness> {

    create(req: express.Request, res: express.Response): void {
        try {

            var person: IPersonModel = <IPersonModel>req.body;
            var personBusiness = new PersonBusiness();
            personBusiness.create(person, (error, result) => {
                if(error) res.send({"error": "error"});
                else res.send({"success": "success"});
            });
        }
        catch (e)  {
            console.log(e);
            res.send({"error": "error in your request"});

        }
    }
    update(req: express.Request, res: express.Response): void {
        try {
            var person: IPersonModel = <IPersonModel>req.body;
            var _id: string = req.params._id;
            var personBusiness = new PersonBusiness();
            personBusiness.update(_id, person, (error, result) => {
                if(error) res.send({"error": "error"});
                else res.send({"success": "success"});
            });
        }
        catch (e)  {
            console.log(e);
            res.send({"error": "error in your request"});

        }
    }
    delete(req: express.Request, res: express.Response): void {
        try {

            var _id: string = req.params._id;
            var personBusiness = new PersonBusiness();
            personBusiness.delete(_id, (error, result) => {
                if(error) res.send({"error": "error"});
                else res.send({"success": "success"});
            });
        }
        catch (e)  {
            console.log(e);
            res.send({"error": "error in your request"});

        }
    }
    retrieve(req: express.Request, res: express.Response): void {
        try {

            var personBusiness = new PersonBusiness();
            personBusiness.retrieve((error, result) => {
                if(error) res.send({"error": "error"});
                else res.send(result);
            });
        }
        catch (e)  {
            console.log(e);
            res.send({"error": "error in your request"});

        }
    }
    findById(req: express.Request, res: express.Response): void {
        try {

            var _id: string = req.params._id;
            var personBusiness = new PersonBusiness();
            personBusiness.findById(_id, (error, result) => {
                if(error) res.send({"error": "error"});
                else res.send(result);
            });
        }
        catch (e)  {
            console.log(e);
            res.send({"error": "error in your request"});

        }
    }
}
export = PersonController;
